package OOP.E_2021_Canvas.Old;

public class Square {
    private int lowerX;
    private int lowerY;
    private int width;

    public Square(int lowerX, int lowerY, int width) {
        this.lowerX = lowerX;
        this.lowerY = lowerY;
        this.width = width;
    }

    public int getLowerX() {
        return lowerX;
    }

    public int getLowerY() {
        return lowerY;
    }

    public int getWidth() {
        return width;
    }
}
